package aula014operadoresmatemáticos;

/**
 *
 * @author eliezer
 */
public class Aula014OperadoresMatemáticos {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        double a = 4 + 1;
        System.out.println(a);

        double b = 4 / 1;
        System.out.println(b);

        double c = 4 * 1;
        System.out.println(c);

        double d = 4 - 1;
        System.out.println(d);
/// o operador modelulo nos diz qual é o resto da divisao
        double e = 4 % 1;
        System.out.println(e);

        int f = 1;
        int g = ++f;
        System.out.println(f);
        System.out.println(g);

    }

}

package aula073.math;

/**
 *
 * @author eliezer
 */
public class Aula073Math {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.println(Math.sin(10));//seno
        System.out.println(Math.cos(10));//cosseno
        System.out.println(Math.tan(45));//tangente
        System.out.println(Math.pow(4, 3));//potencia
        System.out.println(Math.sqrt(25));//raizQuadrada
        System.out.println(Math.cbrt(16));//raizCubica
        System.out.println(Math.hypot(8, 6));//Hypotenusa
    }
    
}
